    Check Disk Space:

    du -sh <directory>: Displays the disk usage of a specific directory in a human-readable format.
    lsblk: Lists information about block devices, including disk partitions and their sizes.
    fdisk -l: Lists partition information for all disks on the system.

    Check RAM Usage:

    top: Provides real-time information about system resource usage, including memory usage.
    htop: An interactive version of top that displays system resource usage in a more user-friendly way.
    cat /proc/meminfo: Displays detailed information about the system's memory usage and statistics.
    vmstat: Reports information about system memory, processes, and other system statistics.

These commands offer additional options and details for monitoring disk space and RAM usage on your system. You can choose the command that best suits your requirements and preferences.